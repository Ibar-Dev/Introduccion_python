# Definición de funciones:
def saludar():
    print("¡Hola!")
# Esta función saludar() simplemente imprime "¡Hola!" cuando se llama.

# Parámetros de funciones:

def saludar(nombre):
    print(f"¡Hola, {nombre}!")
# Esta función saludar(nombre) acepta un parámetro nombre y luego imprime un saludo personalizado con ese nombre.

# Retorno de valores:
def suma(a, b):
    return a + b
# Esta función suma(a, b) toma dos parámetros a y b, los suma y devuelve el resultado utilizando la palabra clave return.

# Funciones lambda:
cuadrado = lambda x: x ** 2
# Esta es una función lambda que toma un argumento x y devuelve su cuadrado.