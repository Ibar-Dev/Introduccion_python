# Las variables son contenedores que se utilizan para almacenar datos en la memoria de un programa. En Python, no es necesario declarar el tipo de variable antes de asignarle un valor.
# Simplemente se crea la variable y se le asigna un valor. En este punto, se aprenderá cómo declarar variables, los tipos de datos básicos (enteros, flotantes, cadenas, booleanos) 
# y las reglas de nombrado de variables.




# Declaración de variables
activad = "Hoy se va a correr 200 kilometros y despues haremos 2 metro de salto."
edad = 25
altura = 1.75
es_estudiante = True

# Impresión de variables
print(activad)
print(edad)
print(altura)
print(es_estudiante)


print(type(activad))
print(type(edad))
print(type(altura))
print(type(es_estudiante))


'''Declaración de variables:

Se están declarando cuatro variables:
activad: Una variable de tipo cadena que contiene una frase.

edad: Una variable de tipo entero que almacena la edad, que en este caso es 25.

altura: Una variable de tipo flotante que representa la altura, que en este caso es 1.75 metros.

es_estudiante: Una variable booleana que indica si la persona es estudiante, establecida en True.


Impresión de variables:
Se están imprimiendo las cuatro variables declaradas utilizando la función print() para mostrar sus valores en la consola.
Tipo de datos:

Después de imprimir las variables, se utiliza la función type() para imprimir el tipo de cada variable. Esto muestra el tipo de dato al que pertenece cada variable.
Entonces, el código básicamente declara algunas variables con diferentes tipos de datos (cadena, entero, flotante y booleano), las imprime en la consola y
luego imprime también los tipos de esas variables para demostrar que se han declarado correctamente.'''